    public void LoadHoaDon() {
        tbm_hoadon.setRowCount(0);
        ArrayList<Order> dsPhieuNhap = QuanLyHoaDonDAO.GetHoaDonThanhCong();

        for (Order item : dsPhieuNhap) {

            Object[] rowData = {item.getOrder_id(), item.getId_ban(), item.getGiamgia(), item.getThanhtien(), item.getNgayGioDat()};
            tbm_hoadon.addRow(rowData);

        }
        tableHoaDon.setModel(tbm_hoadon);
    }

