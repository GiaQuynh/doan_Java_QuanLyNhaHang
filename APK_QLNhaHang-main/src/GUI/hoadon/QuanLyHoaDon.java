    private void btnShowActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnShowActionPerformed
        int selectedRow = tableHoaDon.getSelectedRow();
        if (selectedRow != -1) {
            Object idPhieu = tableHoaDon.getValueAt(selectedRow, 0);
            int id = Integer.parseInt(idPhieu.toString());
            ChiTietHoaDon item = new ChiTietHoaDon(id);
            item.setDefaultCloseOperation(JFrame.DISPOSE_ON_CLOSE);
            item.setVisible(true);
        } else {
            JOptionPane.showMessageDialog(this, "Vui lòng chọn một hóa đơn để xem chi tiết.");
        }
    }//GEN-LAST:event_btnShowActionPerformed

    private void btnXuatHoaDonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnXuatHoaDonActionPerformed
        int selectedRow = tableHoaDon.getSelectedRow();
        if (selectedRow != -1) {
            Object idPhieu = tableHoaDon.getValueAt(selectedRow, 0);
            int id = Integer.parseInt(idPhieu.toString());
            String tenNhanVien = getUser();
            XuatHoaDon(id, tenNhanVien);
        } else {
            JOptionPane.showMessageDialog(this, "Vui lòng chọn một hóa đơn để xem chi tiết.");
        }
    }//GEN-LAST:event_btnXuatHoaDonActionPerformed

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        int selectedRow = tableHoaDon.getSelectedRow();
        if (selectedRow != -1) {
            Object idPhieu = tableHoaDon.getValueAt(selectedRow, 0);
            int id = Integer.parseInt(idPhieu.toString());
            boolean check = QuanLyHoaDonDAO.delete(id);
            if (check == true) {
                JOptionPane.showMessageDialog(this, "Xóa thành công!", "Thông báo", JOptionPane.INFORMATION_MESSAGE);
                LoadHoaDon();
            } else {
                JOptionPane.showMessageDialog(this, "Xóa không thành công!", "Thông báo", JOptionPane.WARNING_MESSAGE);
            }
        } else {
            JOptionPane.showMessageDialog(this, "Vui lòng chọn một hóa đơn để xóa ");
        }


    }//GEN-LAST:event_jButton1ActionPerformed
    public void XuatHoaDon(int idhd, String tenNhanVien) {
        try {
            Hashtable map = new Hashtable();
            map.put("order_id", idhd);
            map.put("employee_name", tenNhanVien);
            JasperReport report = JasperCompileManager.compileReport("src/GUI/doanhthu/rpChiTietHoaDon.jrxml");
            JasperPrint p = JasperFillManager.fillReport(report, map, connect.ketNoiCSDL());
            JasperViewer.viewReport(p, false);
            JasperExportManager.exportReportToPdfFile(p, "test.pdf");
        } catch (Exception ex) {
            System.out.println(ex.getMessage());
        }
    }

